package com.tanay.drivecalci;

public class velocity_time_class {
    private String time;
    private String velocity;



    public velocity_time_class(String t, String v){
        time= t;
        velocity= v;
    }

    public String getTime() {
        return time;
    }

    public String getVelocity() {
        return velocity;
    }
}
